// Excel 365 ribbon dropdown — replaces the native <select> on font family
// and font size. Open state is tracked per-name so two dropdowns can't be
// open at once. Outside-click + Escape close them.
type DropdownName = 'fontFamily' | 'fontSize' | 'borderPreset' | 'borderStyle' | 'margins' | 'orientation' | 'paperSize';
const borderStyle = ref<CellBorderStyle>('thin');
const openDropdown = ref<DropdownName | null>(null);
const toggleDropdown = (name: DropdownName): void => {
  openDropdown.value = openDropdown.value === name ? null : name;
};
const closeDropdown = (): void => {
  openDropdown.value = null;
};
const onDropdownPick = (name: DropdownName, value: string | number): void => {
  if (name === 'fontFamily') onFontFamily(String(value));
  else if (name === 'fontSize') onFontSize(value);
  else if (name === 'borderPreset') onBorderPreset(String(value) as BorderPreset);
  else if (name === 'borderStyle') borderStyle.value = String(value) as CellBorderStyle;
  else if (name === 'margins') {
    if (value === 'custom') props.instance?.openPageSetup();
    else onMarginPreset(String(value) as MarginPreset);
  } else if (name === 'orientation') onPageOrientation(String(value) as PageOrientation);
  else if (name === 'paperSize') onPaperSize(String(value) as PaperSize);
  closeDropdown();
};
const onDocPointerDown = (e: MouseEvent): void => {
  if (openDropdown.value == null) return;
  const target = e.target;
  if (!(target instanceof Element)) return;
  if (target.closest('.demo__rb-dd')) return;
  closeDropdown();
};
const onDocKey = (e: KeyboardEvent): void => {
  if (e.key === 'Escape' && openDropdown.value != null) {
    e.preventDefault();
    closeDropdown();
  }
};
onMounted(() => {
  document.addEventListener('mousedown', onDocPointerDown, true);
  document.addEventListener('keydown', onDocKey, true);
});
onUnmounted(() => {
  document.removeEventListener('mousedown', onDocPointerDown, true);
  document.removeEventListener('keydown', onDocKey, true);
});
